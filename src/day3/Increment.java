package day3;

public class Increment {

	public static void main(String[] args) {
		int a,b,c,d;
		a = 10;
		b = 20;
		c = 25;
		d = 25;
		
		System.out.println("a+b = "+(a+b));
		System.out.println("a-b = "+(a-b));
		System.out.println("a*b = "+(a*b));
		System.out.println("b/a = "+(a/a));
		System.out.println("c%a = "+(c%a));
		System.out.println("a++ = "+(a++));
		System.out.println("a-- = "+(a--));
		System.out.println("d++ = "+(d++));
		System.out.println("++d = "+(++d));
		
	

	}

}
